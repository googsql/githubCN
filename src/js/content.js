const translateInSentence = (rawText) => {
  if (!rawText.includes(" ")) return null;
  let replaced = rawText;
  let changed = false;
  for (const [source, target] of inlineReplacePairs) {
    if (replaced.includes(source)) {
      replaced = replaced.split(source).join(target);
      changed = true;
    }
  }
  return changed ? replaced : null;
};

const translateCoreText = (rawText) => {
  const key = normalizeText(rawText);
  if (!key) return null;
  const exact = exactDataMap.get(key);
  if (exact) return exact;
  const lower = lowercaseDataMap.get(key.toLowerCase());
  if (lower) return lower;
  for (const [regex, replacer] of patternReplacers) {
    const match = key.match(regex);
    if (match) return replacer(match);
  }
  const inlineTranslated = translateInSentence(key);
  if (inlineTranslated && inlineTranslated !== key) return inlineTranslated;
  return null;
};

const shouldTranslateValueAttr = (element) => {
  if (!element || !element.tagName) return false;
  if (element.tagName === "BUTTON") return true;
  if (element.tagName !== "INPUT") return false;
  const type = (element.getAttribute("type") || "").toLowerCase();
  return type === "button" || type === "submit" || type === "reset";
};

const translateAttribute = (element, attr) => {
  if (!element.hasAttribute(attr)) return;
  if (attr === "value" && !shouldTranslateValueAttr(element)) return;
  const origin = element.getAttribute(attr);
  if (!origin) return;
  const translated = translateCoreText(origin);
  if (translated && translated !== origin) {
    element.setAttribute(attr, translated);
  }
};

const translateTextNode = (node) => {
  if (!node || node.nodeType !== Node.TEXT_NODE) return;
  const originalText = node.textContent || "";
  if (!originalText.trim()) return;
  if (
    node.parentElement &&
    node.parentElement.tagName &&
    skipTagNames.has(node.parentElement.tagName)
  ) {
    return;
  }
  const match = originalText.match(/^(\s*)([\s\S]*?)(\s*)$/);
  if (!match) return;
  const translated = translateCoreText(match[2]);
  if (!translated || translated === match[2]) return;
  node.textContent = `${match[1]}${translated}${match[3]}`;
};

const translateElement = (element) => {
  if (!element || element.nodeType !== Node.ELEMENT_NODE) return;
  if (skipTagNames.has(element.tagName)) return;
  attrList.forEach((attr) => translateAttribute(element, attr));
};

const processNodeTree = (rootNode) => {
  if (!rootNode) return;
  if (rootNode.nodeType === Node.TEXT_NODE) {
    translateTextNode(rootNode);
    return;
  }
  if (rootNode.nodeType !== Node.ELEMENT_NODE) return;
  const walker = document.createTreeWalker(rootNode, NodeFilter.SHOW_ALL);
  let currentNode = walker.currentNode;
  while (currentNode) {
    if (currentNode.nodeType === Node.TEXT_NODE) {
      translateTextNode(currentNode);
    } else if (currentNode.nodeType === Node.ELEMENT_NODE) {
      translateElement(currentNode);
    }
    currentNode = walker.nextNode();
  }
};

const MutationObserverConfig = {
  childList: true,
  subtree: true,
  characterData: true,
  attributes: true,
  attributeFilter: attrList,
};

processNodeTree(document.body);

const observer = new MutationObserver((mutations) => {
  mutations.forEach((mutation) => {
    if (mutation.type === "characterData") {
      translateTextNode(mutation.target);
      return;
    }
    if (mutation.type === "attributes" && mutation.target) {
      translateElement(mutation.target);
      return;
    }
    if (mutation.type === "childList" && mutation.addedNodes.length > 0) {
      mutation.addedNodes.forEach((node) => processNodeTree(node));
    }
  });
});

observer.observe(document.body, MutationObserverConfig);
